__all__ = ['comparison']

from random import randint as rnd
from sys import argv

def comparison(lower_limit: int=0, upper_limit: int=100, count: int=10) -> bool:
    num_rand = rnd(lower_limit, upper_limit)
    for _ in range(count):
        num_user = int(input(f'–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ –æ—Ç {lower_limit} –¥–æ {upper_limit}: '))
        if num_user == num_rand:
            return True
        elif num_user < num_rand:
            print(f'–ó–∞–≥–∞–¥–∞–Ω–Ω–æ–µ —á–∏—Å–ª–æ –±–æ–ª—å—à–µ {num_user}')
        elif num_user > num_rand:
            print(f'–ó–∞–≥–∞–¥–∞–Ω–Ω–æ–µ —á–∏—Å–ª–æ –º–µ–Ω—å—à–µ {num_user}')
    else:
        return False

    
if __name__ == '__main__':  # –ó–∞–ø—É—Å–∫ –ø—Ä–æ–≥—Ä–∞–º–º—ã –ø—Ä–æ–≤–µ—Ä–∫–∏ –∏–∑ –¥–∞–Ω–Ω–æ–≥–æ 
    params = (int(num) for num in argv[1:])
    print(comparison(*params))