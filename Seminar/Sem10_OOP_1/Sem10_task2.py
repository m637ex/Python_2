class Rectangle:
    def __init__(self, lenght: int|float, width: int|float|None=None):
        self.lenght = lenght
        if width:               # –ï—Å–ª–∏ –µ—Å—Ç—å —à–∏—Ä–∏–Ω–∞
            self.width = width
        else:
            self.width = lenght
    
    
    def get_perimetr(self):
        return (self.lenght + self.width) * 2
    
    def get_area(self):
        return self.lenght * self.width
    
