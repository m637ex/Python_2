from math import pi 

class Circle:
    def __init__(self, radius): # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –ø—Ä–∏–Ω–∏–º–∞–µ–º—ã–µ –∞—Ä–≥—É–º–µ–Ω—Ç—ã
        self.radius = radius
        
    def perimetr(self):         # –≥–æ—Ç–æ–≤–∏–º –º–µ—Ç–æ–¥ –¥–ª–∏–Ω–∞ –æ–∫—Ä—É–∂–Ω–æ—Å—Ç–∏
        return 2 * pi * self.radius
        
    def area(self):      # –ì–æ—Ç–æ–≤–∏–º –º–µ—Ç–æ–∂ –ø–ª–æ—â–∞–¥—å –∫—Ä—É–≥–∞
        return pi * self.radius ** 2
        

